#!/usr/bin/env node
/**
 * Reads all migration SQL files and generates a TypeScript file
 * exporting SCHEMA_STATEMENTS for use in test helpers.
 *
 * Run: node scripts/generate-test-schema.js
 * Output: src/generated-schema.ts
 */
const fs = require("fs")
const path = require("path")

const migrationsDir = path.join(__dirname, "..", "migrations")
const outputFile = path.join(__dirname, "..", "src", "generated-schema.ts")

const files = fs.readdirSync(migrationsDir)
  .filter((f) => f.endsWith(".sql"))
  .sort()

const statements = []
// Track added columns to skip duplicates from overlapping migration files
const addedColumns = new Set()

for (const file of files) {
  const sql = fs.readFileSync(path.join(migrationsDir, file), "utf-8")
  for (const stmt of sql.split(";")) {
    const trimmed = stmt.trim()
    // Skip empty, comments-only, and PRAGMA lines
    if (!trimmed) continue
    if (trimmed.split("\n").every((line) => line.trim().startsWith("--") || !line.trim())) continue
    if (trimmed.startsWith("PRAGMA")) continue
    // Deduplicate ALTER TABLE ADD COLUMN statements (two 0002_* files overlap)
    const addColMatch = trimmed.match(/ALTER TABLE (\w+) ADD COLUMN (\w+)/i)
    if (addColMatch) {
      const key = `${addColMatch[1]}.${addColMatch[2]}`
      if (addedColumns.has(key)) continue
      addedColumns.add(key)
    }
    statements.push(trimmed)
  }
}

const output = `// AUTO-GENERATED by scripts/generate-test-schema.js — do not edit
// Source: migrations/*.sql

export const SCHEMA_STATEMENTS: string[] = ${JSON.stringify(statements, null, 2)};
`

fs.writeFileSync(outputFile, output, "utf-8")
console.log(`Generated ${outputFile} with ${statements.length} statements from ${files.length} migration file(s)`)
